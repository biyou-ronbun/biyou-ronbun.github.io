// ---------------------------------------------------------------
//  論文台帳の「有害事象」の欄を数える
//
//      node site/adverse-census.mjs
//
//  ---------------------------------------------------------------
//  ★★ なぜ、わざわざコードにするのか（2026-07-14）
//
//    同じ台帳を、3回数えた。**3回とも違う数が出た。**
//
//        1回目  「何も書いていない」 60本
//        2回目  「記載なしで始まる」 55本
//        3回目  「記載なしの語を含む」 69本
//
//    **誰も嘘をついていない。定義が違っただけ。**
//    だが読者から見れば、**再現できない数字は、作り話と区別がつかない。**
//
//    このブログは「一次情報まで辿って、数字はそのまま出す」と言っている。
//    **その数字が、数えるたびに変わってはいけない。**
//
//  ★ だから、定義をここに書く。数字は、このコードが出したものだけを使う。
//    リポジトリは公開なので、**読者は自分で走らせて確かめられる。**
//
//  ★★ 本当の直し方は、これではない
//
//    `adverse` が**自由記述の文章**であることが、そもそもの原因。
//    文章を正規表現で分類している限り、定義は揺れ続ける。
//
//    **直すなら、台帳に構造化された欄を持たせること**（例: adverseReported: yes/no/unknown）。
//    ただし99本すべてを一次情報に当たり直す必要がある。**まだやっていない。**
//    それまでは、この数え方を「唯一の定義」として使う。
// ---------------------------------------------------------------

import { readFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const SITE = dirname(fileURLToPath(import.meta.url));
const papers = JSON.parse(readFileSync(join(SITE, 'papers.json'), 'utf8')).papers;

// ---- 定義（★ ここが唯一の定義。変えたら、過去の数字と比べられなくなる）----------
//
// 上から順に判定する。**最初に当たったものを採る。**

// ① 欄そのものが空
const isEmpty = (a) => !a;

// ② 「確認できなかった」— 全文が有料などで、記載の有無すら分からない
//    ★ 「0件だった」でも「書いていない」でもない。**第3の状態。**
const UNCONFIRMED = /確認できて?いない|確認できず|確認できなかった|全文が有料|要旨が(無|な)い/;

// ③ 「著者が書いていない」
//    ★ これは「副作用が無かった」ではない。**書かなかった、という意味。**
const NOT_WRITTEN = /記載なし|記載が(無|な)い|言及(が)?(無|な)い|記述(が)?(無|な)い|報告(が)?(無|な)い|書かれていない/;

// ④ それ以外 = 何らかの記載がある

const bucket = { 空欄: [], 確認できず: [], 書かれていない: [], 記載あり: [] };

for (const p of papers) {
  const a = (p.adverse ?? '').trim();
  const key = isEmpty(a)
    ? '空欄'
    : UNCONFIRMED.test(a)
      ? '確認できず'
      : NOT_WRITTEN.test(a)
        ? '書かれていない'
        : '記載あり';
  bucket[key].push(p.pmid);
}

// ---- 出力 -----------------------------------------------------------

console.log('');
console.log('='.repeat(64));
console.log(`  論文台帳 ${papers.length} 本の「有害事象」の欄`);
console.log('='.repeat(64));
console.log('');

for (const [k, v] of Object.entries(bucket)) {
  console.log(`  ${String(v.length).padStart(3)} 本   ${k}`);
}

