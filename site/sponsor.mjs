// ---------------------------------------------------------------
//  タイアップ（企業から金を受け取って書いた記事）の器
//
//  ---------------------------------------------------------------
//  ★★ 2026-07-14、オーナー判断でタイアップを開けました。
//
//    それまで、うちはタイアップを断っていました。理由はこうでした:
//
//      **「受けたら『この商品には根拠がありません』と書けなくなります」**
//
//    その理由は、いまも正しい。**だが、オーナーが受けると決めました。**
//
//  ---------------------------------------------------------------
//  ★★ 法律（好みではない。ここは動かせない）
//
//    **景品表示法のステマ規制（2023年10月1日施行）**
//
//      事業者が自らの表示であることを、**消費者が判別困難な表示は、不当表示。**
//      **罰せられるのは広告主ではなく、表示した側です。**
//
//    そして、うちは**この規制について記事を書いています**
//    （「美白美容液の広告に、東京都が措置命令」）。
//    **開示せずにタイアップを出せば、うちが批判した側と同じことをやることになります。**
//
//  ★ だから、この器は「開示を、外せない形」で作ります。
//
//    ・記事の**いちばん上**に出る（下に隠さない）
//    ・**記事一覧のカードにも**出る（クリックする前に分かる）
//    ・**誰から金を受け取ったか、実名で書く**
//    ・**何を約束したか / 何を約束していないか**を書く
//    ・site/verify.mjs が、**開示の無いタイアップ記事を見つけたら、公開を止める**
//
//  ---------------------------------------------------------------
//  ★★ そして、これだけは残します
//
//    **タイアップでも、根拠が見つからなければ「見つからなかった」と書く。**
//
//    それを書けなくなったら、このサイトに読む理由がありません。
//    **書けない条件のタイアップは、受けないこと。**
//
//    ★ 器は作りました。**受けるかどうかは、オーナーだけが決められます。**
//      articles.json に sponsor を書けるのは、オーナーだけです。
// ---------------------------------------------------------------

/**
 * 記事の sponsor 欄の形:
 *
 *   "sponsor": {
 *     "company": "◯◯株式会社",              // ★ 実名。必須
 *     "paid": true,                          // 金銭を受け取ったか
 *     "provided": "商品の無償提供",           // 金銭以外に受け取ったもの（無ければ ""）
 *     "promised": "記事を書くこと",           // ★ 何を約束したか
 *     "notPromised": "結論の内容。根拠が見つからなければ、そう書きます", // ★ 何を約束していないか
 *     "reviewedByCompany": false             // ★ 公開前に企業が原稿を見たか
 *   }
 */

export const SPONSOR_REQUIRED = ['company', 'promised', 'notPromised'];

/** 開示の HTML。**記事のいちばん上に出す。** */
export function sponsorBanner(s, escapeHtml) {
  if (!s) return '';
  const bits = [];
  if (s.paid) bits.push('金銭を受け取っています');
  if (s.provided) bits.push(`${escapeHtml(s.provided)}を受け取っています`);
  const got = bits.length ? bits.join('。') : '金銭・物品は受け取っていません';

  return `<aside class="pr">
  <p class="pr-tag">PR（タイアップ記事）</p>
  <p class="pr-body"><strong>この記事は ${escapeHtml(s.company)} から依頼を受けて書いています。${escapeHtml(got)}。</strong></p>
  <p class="pr-body"><strong>約束したこと:</strong> ${escapeHtml(s.promised)}</p>
  <p class="pr-body"><strong>約束していないこと:</strong> ${escapeHtml(s.notPromised)}</p>
  <p class="pr-body"><strong>公開前に ${escapeHtml(s.company)} が原稿を見たか:</strong> ${s.reviewedByCompany ? '<strong>見ています。</strong>' : 'いいえ。見ていません。'}</p>
  <p class="pr-note">景品表示法（2023年10月〜）は、広告だと分からない広告を禁じています。<strong>だから、隠さずに書いています。</strong></p>
</aside>`;
}

/** 記事一覧のカードに出す札。**クリックする前に分かるように。** */
export function sponsorChip(s) {
  return s ? '<span class="card-pr">PR</span>' : '';
}
