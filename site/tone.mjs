// ---------------------------------------------------------------
//  「言い切っているのか、言えないと断っているのか」を判定する
//
//  ★ ここは、3つの関門（記事 / X / note・はてな）が共通で使います。
//
//    以前は3つが別々に実装していて、**3つとも別々の穴を持っていました。**
//    「とは書けません」は通すのに「とも言えません」で止まる、といった具合に。
//    そして実際に、正しく書かれた記事を止めかけました。
//
//    判定を1か所にまとめます。**穴を塞ぐなら、3か所ではなく1か所で塞ぐ。**
//
//  ---------------------------------------------------------------
//  何を止めるのか
//
//    ✗ 止める : 飲んでも効かないので、やめましょう
//    ✗ 止める : 効果がないことが分かりました
//
//    ○ 通す  : ただし、「効かない」とは書けません
//    ○ 通す  : 「撤回されたから効果がない」とも言えません
//    ○ 通す  : 効かない証拠ではなく、誰も検算していない状態です
//
//  **このブログの記事の大半は「ヒト試験が見つかりませんでした」で終わります。**
//  **だから、言い過ぎを断る文が、記事の中で最も多く出てきます。**
//  **それを関門が止めるなら、間違っているのは関門のほうです。**
// ---------------------------------------------------------------

// 言い過ぎの語（これ自体は、含まれているだけでは止めない）
export const OVERREACH_WORDS = [
  '効かない',
  '効果がない',
  '効果はない',
  '無意味',
  'やめるべき',
  '避けるべき',
  '買ってはいけない',
  '使ってはいけない',
];

// 打ち消しの言い方。**列挙は必ず漏れるので、これだけに頼らない**（下の「引用の中か」と併用する）
const NEGATION = [
  'とは言えません', 'とも言えません', 'とは言えない', 'とも言えない', 'とは言い切れません',
  'とは書けません', 'とも書けません', 'とは書けない',
  'とは限りません', 'とは限らない',
  'ではありません', 'ではない',
  'わけではありません', 'わけではない',
  'ことではありません', 'ことではない',
  '証拠ではなく', '証拠にはなりません', '証拠にはならない',
  '持っていません', '言いません', 'なりません',
  '別のことです', '別です', '別の話です',
  '早すぎます', '早すぎる',
  'と断定', 'と決めつけ',
];

// その位置が「」の中にあるか。
//
// ★ これが、いちばん効く信号です。
//   記事は「言えないこと」を **引用して否定します**。
//   「効かない」とは書けません／「効かない証拠」だとは言いません／
//   「無意味だ」ではありません——**どれも語が「」の中にあります。**
//
//   引用の中の語は、**その人が言っていることではありません。** 打ち消しの言い方を
//   何十通り列挙するより、この1点を見るほうが確実です。
const inQuote = (text, pos) => {
  // 同じ段落の中だけを見る（段落をまたいだ「」は数えない）
  const start = text.lastIndexOf('\n\n', pos) + 1;
  const before = text.slice(start, pos);
  const open = (before.match(/[「『]/g) ?? []).length;
  const close = (before.match(/[」』]/g) ?? []).length;
  return open > close;
};

/**
 * その文が、言い過ぎを「言い切っている」か。
 * 引用の中にある語、打ち消しが続く語は、言い切っていない（＝通す）。
 *
 * @returns {null | {word: string, context: string}} 言い切っていれば、その語と前後
 */
export function assertsOverreach(text) {
  for (const w of OVERREACH_WORDS) {
    let i = -1;
    while ((i = text.indexOf(w, i + 1)) >= 0) {
      if (inQuote(text, i)) continue; // 引用の中 → その人の主張ではない

      const after = text.slice(i + w.length, i + w.length + 28);
      if (NEGATION.some((n) => after.includes(n))) continue; // 打ち消している

      return {
        word: w,
        context: text.slice(Math.max(0, i - 30), i + w.length + 30).replace(/\n/g, ' '),
      };
    }
  }
  return null;
}
