// ---------------------------------------------------------------
//  X の投稿を、出す前に検査する
//
//    node auto/verify-x.mjs          未投稿ぜんぶを検査する
//    node auto/verify-x.mjs --id 42  1件だけ検査する
//
//  post-x.mjs が、投稿の直前にこれを呼びます。
//  **落ちた投稿は、投稿されません。**
//
//  ---------------------------------------------------------------
//  なぜ要るのか
//
//    記事は verify.mjs が止められます。公開前に機械が読むからです。
//    **X の投稿には、その関門がありませんでした。**
//
//    そして X の投稿は、記事より危ない。
//      ・取り消せない（消しても、スクリーンショットは残る）
//      ・短いので、「ここまでは言えません」が削られやすい
//      ・伸ばそうとすると、断定に寄る力が働く
//
//    このブログの武器は「根拠がある」と「誠実である」の2つだけです。
//    X で断定した瞬間、記事側でどれだけ慎重に書いても意味がなくなります。
//  ---------------------------------------------------------------

import { readFileSync, existsSync } from 'node:fs';
import { assertsOverreach } from '../site/tone.mjs';
import { checkEnglish } from '../site/tone-en.mjs';
import { loadCorpus, checkPost } from './x-numbers.mjs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const QUEUE = join(ROOT, 'x', 'queue.json');

// ---- 薬機法（site/verify.mjs と同じ。ここを緩めないこと） ----------
const FORBIDDEN = [
  'シミが消える', 'シミが取れる', 'シワが消える', 'シワがなくなる',
  '肌が若返る', '若返り効果', 'アンチエイジング効果',
  '細胞が生まれ変わる', '肌が再生する',
  'デトックス', '毒素を排出',
  '副作用はありません', '副作用がない', '100%安全', '絶対に安全',
  '必ず効きます', '絶対に効く', '誰にでも効く',
  'ニキビが治る', 'アトピーが治る',
  '医師も推奨', '医師が推奨',
];

// ---- 煽り（X で最も出やすい事故） ---------------------------------
//
// 「根拠が無かった」と「騙されている」は、別のことです。
// 前者は事実の報告。後者は相手の意図の断定で、こちらには確かめる手段がない。
const FATAL = [
  '嘘', 'デタラメ', 'でたらめ', '詐欺', 'インチキ', '騙し', '騙さ', '騙され',
  'ぼったくり', '悪質', '許せない', 'ひどい', '搾取',
];

// ---- 言い過ぎ（根拠が無い ≠ 効かない） ----------------------------
//
// 「その主張の出典が見つからなかった」は言える。
// 「効かない」は、効かないことを示した試験がなければ言えない。
// これはこのブログが記事で最も気をつけていることで、X でも同じです。
const OVERREACH = [
  '効かない', '効果がない', '効果はない', '無意味', '無駄',
  'やめるべき', '避けるべき', '買ってはいけない', '使ってはいけない',
];

// ---- 「言っている」のか「言えないと断っている」のか --------------------
//
// ★ ここが、この検査でいちばん大事なところです。
//
//   「効かない」を含むからといって、止めてはいけません。
//
//     ✗ 止めるべき : 飲んでも効かないので、やめましょう
//     ○ 通すべき  : ただし、「効かない」とは書けません
//     ○ 通すべき  : 効かない証拠ではなく、誰も検算していない状態です
//
//   後者は、このブログが最も大事にしている書き方です。
//   **言い過ぎを断っている文を関門が止めるなら、間違っているのは関門のほうです。**
//
//   だから、その語の直後に「打ち消し」が続いているかを見ます。
//
// （2026-07-13、既存の投稿57本を検査して見つかった。4件すべてが、この形の誤検知だった）

const NEGATED = [
  'とは言えません', 'とも言えません', 'とは言えない', 'とも言えない', 'とは言い切れません',
  'とは書けません', 'とも書けません', 'とは書けない',
  'とは限りません', 'とは限らない',
  'ではありません', 'ではない',
  'わけではありません', 'わけではない',
  '証拠ではなく', '証拠にはなりません', '証拠にはならない',
  'と断定', 'と決めつけ',
];

// その語のうしろ 24 文字以内に打ち消しが来ていれば、断っている文とみなす。
// 「」や 、 をまたぐので、直後だけを見ると取りこぼす。
const isNegated = (text, word) => {
  let i = -1;
  while ((i = text.indexOf(word, i + 1)) >= 0) {
    const after = text.slice(i + word.length, i + word.length + 24);
    if (!NEGATED.some((n) => after.includes(n))) return false; // 1つでも「言い切っている」箇所があれば駄目
  }
  return true;
};

// ---- 読者を主語にしていないか --------------------------------------
//
// ★ ここは、上の FATAL / OVERREACH では捕まりません。
//
//   **「あなたは騙されていました」は、禁止語を1語も使わずに書けます。**
//   煽りの語も、言い過ぎの語も、薬機法の語も、1つも使わずに。
//
//   そして、これをやった瞬間、読者は去ります。
//   うちの投稿は「あなたが信じていたことには根拠がなかった」という構造を持っています。
//   **一歩間違えると「騙されてましたね」になります。**
//
//   **読者は敵ではありません。同じものを調べている仲間です。**
//
// ★ 規則: **読者を主語にしない。出典を主語にする。**
//
//     ✗ あなたが信じていた「28日周期」に、根拠はありませんでした
//     ○ 「28日周期」の出典を辿りました。実測した論文は、見つかりませんでした
//
//   前者は読者を裁いています。後者は、出典について語っているだけです。
//
// （X のアルゴリズムの重みでも、ブロックは -74.0、報告は -369.0。
//   いいねの +0.5 とは桁が違います。**煽りは、算数でも割に合いません**）

const READER_AS_SUBJECT = [
  'あなたは騙され', 'あなたも騙され', '騙されていました', '騙されていた',
  'あなたが信じて', '信じ込まされ', '思い込まされ',
  '知らないと損', '知らない人は損', '知らないのは',
  '情弱', 'まだ使ってるの', 'まだ信じてるの', 'まだ買ってるの',
  '9割が知らない', '9割は知らない', '99%の人',
  'こんな人は要注意', 'こういう人は危険',
];

// ---- 文字数（X の数え方） ------------------------------------------
// 日本語は1文字＝2カウント、URLは長さに関わらず23カウント固定、上限280。
const countX = (text) => {
  const withoutUrls = text.replace(/https?:\/\/\S+/g, '');
  const urls = (text.match(/https?:\/\/\S+/g) ?? []).length;
  let n = 0;
  for (const ch of withoutUrls) {
    n += /[\u0000-ჿ -‍‐-‟′-‷]/.test(ch) ? 1 : 2;
  }
  return n + urls * 23;
};

// ---- 検査 -----------------------------------------------------------

const queue = JSON.parse(readFileSync(QUEUE, 'utf8'));

const onlyId = (() => {
  const i = process.argv.indexOf('--id');
  return i >= 0 ? process.argv[i + 1] : null;
})();

const targets = queue.posts.filter(
  (p) => p.status === 'pending' && (!onlyId || String(p.id) === String(onlyId))
);

const failures = [];

// 記事（＋その記事が埋め込んでいる図）を、投稿の数値と突き合わせるために読む
const corpus = loadCorpus();

for (const p of targets) {
  const where = `#${p.id} (${p.at})`;
  const t = p.text ?? '';

  if (!t.trim()) {
    failures.push(`${where}: 本文が空です`);
    continue;
  }

  const n = countX(t);
  if (n > 280) {
    failures.push(`${where}: 長すぎます（${n} / 280）`);
  }

  for (const w of FORBIDDEN) {
    if (t.includes(w)) {
      failures.push(`${where}: 薬機法に触れる表現「${w}」\n      ${t.split('\n')[0]}`);
    }
  }

  for (const w of FATAL) {
    if (t.includes(w) && !isNegated(t, w)) {
      failures.push(
        `${where}: 煽り「${w}」\n` +
          `      「根拠が見つからなかった」と「騙されている」は別のことです。\n` +
          `      前者は事実の報告、後者は相手の意図の断定で、確かめる手段がありません。\n` +
          `      ${t.split('\n')[0]}`
      );
    }
  }

  for (const w of READER_AS_SUBJECT) {
    if (t.includes(w)) {
      failures.push(
        `${where}: 読者を主語にしています「${w}」\n` +
          `      **読者は敵ではありません。同じものを調べている仲間です。**\n` +
          `      「あなたが信じていたことには根拠がなかった」——これを読者に向けた瞬間、\n` +
          `      それは「騙されてましたね」になります。読者は去ります。\n` +
          `\n` +
          `      ✗ あなたが信じていた「28日周期」に、根拠はありませんでした\n` +
          `      ○ 「28日周期」の出典を辿りました。実測した論文は、見つかりませんでした\n` +
          `\n` +
          `      **読者を主語にしない。出典を主語にする。**\n` +
          `      ${t.split('\n')[0]}`
      );
    }
  }

  // 言い過ぎの判定は site/tone.mjs に一本化してある。
  // （記事 / X / note・はてな の3つが別々に実装していて、3つとも別々の穴を持っていた。
  //   「とは書けません」は通すのに「とも言えません」で止まる、といった具合に）
  const over = assertsOverreach(t);
  if (over) {
    failures.push(
      `${where}: 言い過ぎ「${over.word}」\n` +
        `      「出典が見つからなかった」は言えます。「効かない」は言えません。\n` +
        `      効かないことを示した試験が要ります。\n` +
        `      （「『効かない』とは書けません」のように断っている文なら、この検査は通ります）\n` +
        `      …${over.context}…`
    );
  }

  // ---- 英語の投稿 ----------------------------------------------------
  //
  // ★ 上の検査は、すべて日本語しか読めません。
  //   英語で "This ingredient doesn't work." と書いても、1つも引っかかりません。
  //
  //   **関門を通していない投稿を出すことは、うちが批判してきた形そのものです。**
  //
  //   英語が含まれる投稿は、英語の関門（site/tone-en.mjs）にもかけます。
  //   （日本語の記事と同じ判定を、英語でもやる）

  const isEnglish = /[a-zA-Z]/.test(t) && (t.match(/[a-zA-Z]/g) ?? []).length > t.length * 0.5;

  if (isEnglish) {
    for (const problem of checkEnglish(t)) {
      // 「うちの記事であることを示す言葉が無い」は、X の投稿には求めない。
      // 280字にそれを全部入れるのは無理で、入れさせると煽りに寄る。
      if (problem.startsWith('この記事には')) continue;
      failures.push(`${where} [英語]: ${problem}`);
    }
  }

  // 画像を指定しているなら、実在すること
  if (p.image) {
    const img = join(ROOT, p.image.replace(/^[./]+/, ''));
    if (!existsSync(img)) {
      failures.push(`${where}: 画像がありません: ${p.image}`);
    }
  }

  // ★★ 投稿に書かれた数値が、記事に実在すること
  //
  //   **この関門は、articles/ を一度も開いていなかった。**
  //   x/queue.json しか読まないので、記事に無い数字を書いても通った。
  //
  //   2026-07-14、実際に出かけていた:
  //       投稿「1本目: 著者7人中**5人**がメーカー社員」
  //       記事「著者7人のうち**6人**が、キユーピー株式会社」
  //   訂正が記事・カード・図に入って、**X の下書きだけが古いまま残っていた。**
  //   投稿予定は 2026-07-16 15:00。**次の実行で出るところだった。**
  const num = checkPost(t, corpus);
  if (!num.ok) {
    failures.push(
      `${where}: **この投稿の数値が全部そろっている記事が、1本もありません**\n` +
        `      投稿が主張している数値: ${num.tokens.join(' / ')}\n` +
        `      いちばん惜しい記事: articles/${num.slug}.md（そこに無い: ${num.missing.join(' / ')}）\n` +
        `      ★ ただし「いちばん惜しい記事」が正しい記事とは限りません。**投稿の全数値を、記事と突き合わせること。**\n` +
        `      ★ 記事が訂正されて、投稿だけ古いままかもしれません。**記事のほうを先に確かめること。**`
    );
  }
}

// ---- 結果 -----------------------------------------------------------

if (failures.length) {
  console.error('');
  console.error('='.repeat(41));
  console.error(`  投稿を止めます。${failures.length} 件の問題があります。`);
  console.error('='.repeat(41));
  console.error('');
  failures.forEach((f) => console.error(`  ✗ ${f}\n`));
  console.error('x/queue.json を直すまで、この投稿は出ません。');
  console.error('');
  process.exit(1);
}

console.log(`X の検査を通過しました（未投稿 ${targets.length} 件）`);
